package com.javarush.task.task04.task0438;

/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        for (int i = 1; i < 22; i++) {
            if (i <= 10) {
                System.out.print(8);
            } else if (i > 10) {
                System.out.println(8);
            }
        }
    }
}
